import java.util.Scanner
fun main() {
    var read = Scanner(System.`in`)

    println("Enter num1")
    var num1=read.nextFloat()
    println(num1)

    println("Enter num2")
    var num2=read.nextFloat()
    println(num2)

    println("addition")
    var add=(num1+num2)
    println(add)

    println("subtraction")
    var subtract=(num1-num2)
    println(subtract)

    println("division")
    var divide=(num1/num2)
    println(divide)

    println("multplication")
    var multiply=(num1*num2)
    println(multiply)






}










